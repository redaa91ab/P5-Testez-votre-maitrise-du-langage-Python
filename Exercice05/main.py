def sum(a, b):
    """
    Add b to a

    Parameters :
    a : int or float
    b : int or float

    Returns :
    Result of a + b : int or float
    
    """
    return a + b
 
def subtraction(a, b):
    """
    Subtract b from a

    Parameters :
    a : int or float
    b : int or float

    Returns : 
    Results of a - b : int or float
    
    """
    return a - b